  static List<Letter> generateAlphabet() {
    List<Letter> fullAlphabet = [];

    for (String char in pedagogicalSequence) {
      String normalizedChar = char.toLowerCase();
      // Map latin typoes provided in the prompt to the right Cyrillic characters for word lookup
      if (normalizedChar == 't') normalizedChar = 'т';
      if (normalizedChar == 'ž') normalizedChar = 'ж';

      LetterType type = LetterType.consonant;
      if (['а', 'е', 'и', 'і', 'о', 'у', 'я', 'ю', 'є', 'ї'].contains(normalizedChar)) {
        type = LetterType.vowel;
      } else if (normalizedChar == 'ь') {
        type = LetterType.special;
      }

      bool isUpper = char != char.toLowerCase();

      List<String> words = letterWords[normalizedChar] ?? ['слово', 'тест'];
      if (isUpper) {
        words = words.map((w) => w.toUpperCase()).toList();
      }

      fullAlphabet.add(Letter(
        character: char,
        isUppercase: isUpper,
        type: type,
        words: words,
      ));
    }

    return fullAlphabet;
  }
